console.log("======== Post Increment ========");
var x = 10;
console.log(x++);
console.log(x);

console.log("======== Pre Increment ========");
var y = 10;
console.log(++y);
console.log(y);

console.log("======== Post Decrement ========");
var z = 10;
console.log(z--);
console.log(z);

console.log("======== Pre Decrement ========");
var a = 10;
console.log(--a);
console.log(a);