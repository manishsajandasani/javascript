let x;

// Square root
console.log("======== Square Root ========");
x = Math.sqrt(9);
console.log(x);

// Absolute value
console.log("======== Absolute Value ========");
x = Math.abs(-5);
console.log(x);

// Round
console.log("======== Round ========");
x = Math.round(4.267);
console.log(x);

// Round up
console.log("======== Round Up ========");
x = Math.ceil(4.2);
console.log(x);

// Round down
console.log("======== Round Down ========");
x = Math.floor(4.9);
console.log(x);

// Exponent
console.log("======== Exponent ========");
x = Math.pow(2, 3);
console.log(x);

// Minimum number
console.log("======== Minimum Number ========");
x = Math.min(4, 5, 3);
console.log(x);

// Maximum number
console.log("======== Maximum Number ========");
x = Math.max(4, 5, 3);
console.log(x);

// Get a random number/decimal between 0 and 1
console.log("======== Random Digital Number between 0 and 1) ========");
x = Math.random();
console.log(x);

// Get a random number between 1 and 100
console.log("======== Random Number between 1 and 100 ========");
x = Math.floor(Math.random() * 100 + 1);
console.log(x);

